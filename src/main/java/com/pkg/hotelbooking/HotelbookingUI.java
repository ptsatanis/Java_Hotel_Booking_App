    private static class MultiLineCellRenderer extends DefaultListCellRenderer {
        @Override
        public Component getListCellRendererComponent(JList<?> list, Object value, int index,
                                                      boolean isSelected, boolean cellHasFocus) {
            Component renderer = super.getListCellRendererComponent(list, value, index, isSelected, cellHasFocus);
            if (renderer instanceof JLabel) {
                JLabel label = (JLabel) renderer;
                label.setText("<html>" + stylizeText(value.toString()) + "</html>");
            }
            if (isSelected) {
                renderer.setBackground(new java.awt.Color(245, 245,240));
                renderer.setForeground(Color.BLACK);
            }
            return renderer;
        }

        private String stylizeText(String text) {
            String[] words = text.split("\\u2605", 2);
            if (words.length > 1) {
                String firstWord = words[0];
                String restOfString = words[1];
                char c = '\u2605';
                return "<span style='font-size:130%'>" + firstWord +"</span>"+c+ restOfString.replace("\n", "<br>");
            }
            words = text.split("\\s", 2);
            if(words.length>1){
                if(words[0].equalsIgnoreCase("No"))
                    return text;
                if(words[0].equalsIgnoreCase("room")){
                    String[] temp=words[1].split("\\s",2);
                    return "<span style='font-size:130%'>" + words[0]+" "+temp[0] + "<br></span>" + temp[1].replace("\n", "<br>");
                }
            }
            return text;
        }
    }
    
